package globalmanufacturing_v1.globalmanufacturing_v1;

public class WCPerformance {

	public String wc;
	public String perf;
	
	public WCPerformance() {
		
	}

	public WCPerformance(String wc, String perf) {
		super();
		this.wc = wc;
		this.perf = perf;
	}

	public String getWc() {
		return wc;
	}

	public void setWc(String wc) {
		this.wc = wc;
	}

	public String getPerf() {
		return perf;
	}

	public void setPerf(String perf) {
		this.perf = perf;
	}
	
}
